# Syntax for if else

num = 15

# Example 1

if num % 2 == 0:
    print("even")
else:
    print("odd")

# Example 2

if num > 10:
    print("greater than 10")
else:
    print("less than or equal to 10")

# Syntax for if elseif

if num % 2 == 0:
    print("even")
elif num == 15:
    print("number is 15")
else:
    print("the last condition")
